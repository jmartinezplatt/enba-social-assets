function getProductionPieces() {
  const byId = new Map(globalThis.ENBATiktokPieces.map((piece) => [piece.id, piece]));
  return ENBA_TOP10_PRODUCTION.map((item) => ({
    ...byId.get(item.id),
    ...item
  }));
}

function renderTop10Production() {
  const pieces = getProductionPieces();
  const container = document.getElementById("top10-pieces");
  const stats = {
    total: pieces.length,
    p1: pieces.filter((piece) => piece.priority === "P1").length,
    p2: pieces.filter((piece) => piece.priority === "P2").length
  };

  document.getElementById("top10-total").textContent = String(stats.total);
  document.getElementById("top10-p1").textContent = String(stats.p1);
  document.getElementById("top10-p2").textContent = String(stats.p2);

  container.innerHTML = pieces.map((piece) => {
    const media = `<video src="${piece.media}" ${piece.poster ? `poster="${piece.poster}"` : ""} autoplay muted loop playsinline preload="metadata"></video>`;
    const screenText = piece.screenTextFinal.map((line) => `<div class="hook-line">${line}</div>`).join("");
    const screenList = piece.screenTextFinal.map((line) => `<li>${line}</li>`).join("");
    const cutList = piece.cutPlan.map((step) => `<li>${step}</li>`).join("");

    return `
      <article class="piece-card finalist-card">
        <div class="phone-preview">
          ${media}
          <div class="overlay">
            <div class="badge-row">
              <span class="badge">${piece.priority}</span>
              <span class="badge">${piece.pillar}</span>
              <span class="badge">${piece.targetDuration}</span>
            </div>
            <div class="hook-stack">${screenText}</div>
          </div>
        </div>
        <div class="piece-body">
          <div class="piece-kicker">
            <div class="piece-id">${piece.id}</div>
            <div class="piece-format">${piece.format}</div>
          </div>
          <h3 class="piece-title">${piece.title}</h3>
          <p class="piece-hook">${piece.why}</p>
          <div class="meta-grid production-grid">
            <div class="meta-block">
              <div class="meta-label">Objetivo de edición</div>
              <div class="meta-text">${piece.objective}</div>
            </div>
            <div class="meta-block">
              <div class="meta-label">Plano de apertura</div>
              <div class="meta-text">${piece.openingShot}</div>
            </div>
            <div class="meta-block">
              <div class="meta-label">Secuencia de cortes</div>
              <ul class="meta-list">${cutList}</ul>
            </div>
            <div class="meta-block">
              <div class="meta-label">Texto en pantalla final</div>
              <ul class="meta-list">${screenList}</ul>
            </div>
            <div class="meta-block">
              <div class="meta-label">Voiceover sugerido</div>
              <div class="meta-text">${piece.voiceover}</div>
            </div>
            <div class="meta-block">
              <div class="meta-label">Caption borrador</div>
              <div class="meta-text">${piece.captionDraft}</div>
            </div>
            <div class="meta-block">
              <div class="meta-label">CTA final</div>
              <div class="meta-text">${piece.ctaFinal}</div>
            </div>
            <div class="meta-block">
              <div class="meta-label">Dirección de edición</div>
              <div class="meta-text">${piece.editDirection}</div>
            </div>
            <div class="meta-block">
              <div class="meta-label">Asset</div>
              <div class="meta-text">${piece.assetNote}</div>
            </div>
          </div>
        </div>
      </article>
    `;
  }).join("");
}

document.addEventListener("DOMContentLoaded", renderTop10Production);
